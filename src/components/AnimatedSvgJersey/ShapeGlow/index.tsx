function ShapeGlow({
  sport = "football",
  w,
  h,
  colors,
  opacity,
  spread = 3.0, // how much to push the glow outward (in SVG px)
  softness = 10, // blur strength (in SVG px)
  outerOpacity = 0.75, // max halo opacity at dwell
  innerOpacity = 0.28,
}: {
  sport?: SportVariant;
  w: number;
  h: number;
  colors: { c1: string; c2: string; c3: string };
  opacity: MotionValue<number>; // MotionValue<number> 0..1 (we multiply by outerOpacity)
  spread?: number;
  softness?: number;
  outerOpacity?: number;
  innerOpacity?: number;
}) {
  const id = React.useId();
  const outline = OUTLINES[sport];

  // Expand filter region generously so the blur never clips
  const PAD_X = outline.width * 1.5;
  const PAD_Y = outline.height * 1.5;

  return (
    <motion.div
      style={{
        position: "absolute",
        inset: 0,
        pointerEvents: "none",
        opacity, // gradual fade driven by rotateY mapping
        // ensure the glow can paint outside the svg box
        overflow: "visible",
        // optional slight upscaling to make halo more visible
        transform: "translateZ(0)",
      }}
    >
      <svg
        width={w}
        height={h}
        viewBox={`0 0 ${outline.width} ${outline.height}`}
        style={{ overflow: "visible" }} // critical to avoid clipping
      >
        <defs>
          {/* Gradient along the contour */}
          <linearGradient
            id={`lg-${id}`}
            x1="0"
            y1="0"
            x2={outline.width}
            y2={outline.height}
            gradientUnits="userSpaceOnUse"
          >
            <stop offset="0%" stopColor={colors.c2} />
            <stop offset="50%" stopColor={colors.c1} />
            <stop offset="100%" stopColor={colors.c3} />
          </linearGradient>

          {/* OUTER HALO: expand shape (dilate) then blur; huge region so no crop */}
          <filter
            id={`halo-${id}`}
            filterUnits="userSpaceOnUse"
            x={-PAD_X}
            y={-PAD_Y}
            width={outline.width + PAD_X * 2}
            height={outline.height + PAD_Y * 2}
          >
            {/* take the filled path, dilate it, then blur it */}
            <feMorphology
              in="SourceGraphic"
              operator="dilate"
              radius={spread}
              result="spread"
            />
            <feGaussianBlur in="spread" stdDeviation={softness} result="blur" />
            {/* optional: remove original fill to keep just outer ring glow
                <feComposite in="blur" in2="SourceGraphic" operator="out" result="ring" /> */}
          </filter>
        </defs>

        {/* Soft outer halo (filled path → dilate → blur) */}
        <g filter={`url(#halo-${id})`} opacity={outerOpacity}>
          <path d={outline.d} fill={`url(#lg-${id})`} />
        </g>

        {/* Thin inner rim (crisp), adds definition close to edge */}
        <path
          d={outline.d}
          fill="none"
          stroke={`url(#lg-${id})`}
          strokeWidth={1.2}
          strokeLinecap="round"
          strokeLinejoin="round"
          opacity={innerOpacity}
        />
      </svg>
    </motion.div>
  );
}

